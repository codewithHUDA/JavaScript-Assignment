/*Ex# 02: Now Assign and Re-assign The Value In 3 Of Them And See What Result You Get ?*/

      //Method 1.
       var myNum=1; //assigning.

       myNum=2; //re-assigning.

       console.log(myNum); //result is 2.


      //Method 2.
       let myNum1=3; //assigning.

       myNum1=4; //re-assigning.

       console.log(myNum1); //result is 4.


      //Method 3.
       const myNum2 = 5; //assigning.

       myNum2=6; //re-assigning.

       console.log(myNum2); //results in ERROR because values cannot be re-assigned in constant variables.




